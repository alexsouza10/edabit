public class FlipTheIntBoolean {
    public static void main(String[] args) {
        int flipIntBool = reverseNum(1);
        System.out.println(flipIntBool);

    }

    static boolean reverseNum(int num) {
        int false = 1;
        int true = 0;
        
        if(num == false) {
            return true;
        }else {
            return false;
        }
        return num;
}
}
